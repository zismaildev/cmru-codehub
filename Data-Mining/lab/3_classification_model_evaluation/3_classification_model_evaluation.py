# Load libraries
import pandas as pd
from sklearn.tree import DecisionTreeClassifier # Import Decision Tree Classifier
from sklearn.model_selection import train_test_split # Import train_test_split function
from sklearn import metrics #Import scikit-learn metrics module for accuracy calculation

col_names = ['id','sepalLength', 'SepalWidth', 'petalLength', 'petalWidth', 'species']
# load dataset
data1 = pd.read_csv("/content/Iris.csv", header=0, names=col_names)
data1.drop(data1.columns[0], axis=1, inplace=True)
data1

#split dataset in features and target variable
features = ['sepalLength', 'SepalWidth', 'petalLength', 'petalWidth']
X = data1[features] # Features
y = data1.species # Target variable

# Split dataset into training set and test set
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=1) # 70% training and 30% test

# Create Decision Tree classifer object
clf = DecisionTreeClassifier()

# Train Decision Tree Classifer
clf = clf.fit(X_train,y_train)

#Predict the response for test dataset
y_pred = clf.predict(X_test)

# Model Accuracy, how often is the classifier correct?
print("Accuracy:",metrics.accuracy_score(y_test, y_pred))

from sklearn.tree import export_graphviz
